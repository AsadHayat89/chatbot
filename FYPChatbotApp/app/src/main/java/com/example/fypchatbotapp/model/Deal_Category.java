package com.example.fypchatbotapp.model;

public class Deal_Category {

    String Image, Name;

    public Deal_Category() {
    }
    public Deal_Category(String image, String name) {
        Image = image;
        Name = name;
    }

    public String getImage() {
        return Image;
    }

    public void setImage(String image) {
        Image = image;
    }
    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }



}
